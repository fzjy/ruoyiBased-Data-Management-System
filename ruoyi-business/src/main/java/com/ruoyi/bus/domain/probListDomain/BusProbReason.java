package com.ruoyi.bus.domain.probListDomain;

public class BusProbReason {

    private int probReasonId;

    private int probProcessId;

    private String probReason;


    public int getProbReasonId() {
        return probReasonId;
    }

    public void setProbReasonId(int probReasonId) {
        this.probReasonId = probReasonId;
    }

    public int getProbProcessId() {
        return probProcessId;
    }

    public void setProbProcessId(int probProcessId) {
        this.probProcessId = probProcessId;
    }

    public String getProbReason() {
        return probReason;
    }

    public void setProbReason(String probReason) {
        this.probReason = probReason;
    }

}
